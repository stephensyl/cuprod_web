    <h1 id="albumtitle">Annual Album Release Party</h1>
    <p>The Annual Album Release Party is the highlight of the Club Cornell Music Production calendar. Each year, the
        club compiles and produces an album featuring original tracks created by its members. This album showcases the
        diverse talents and creative voices within the club, spanning various genres and styles. The release party is a
        celebration of this collective effort, featuring live performances of the tracks, DJ sets, and listening
        sessions. It's an exciting event that draws attention from the broader Cornell community and beyond,
        highlighting the club's commitment to nurturing musical talent and creativity. The Annual Album Release Party is
        not just about celebrating the finished product, but also about recognizing the hard work, dedication, and
        collaboration that went into its creation.</p>
        
